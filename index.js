function reverseAnim(idName){
    element = document.getElementById(idName);
    element.style.animationDuration = "1s"
    element.style.animationName = "reverseIcon";
}

function hoverAnim(idName){
    element = document.getElementById(idName);
    element.style.animationDuration = "1s";
    element.style.animationName = "iconAnim";
}